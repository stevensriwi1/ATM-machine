    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        switch(currentCard)
        {
            case "loginCard":
            {
                if(isValidAccount(textField1.getText(), new String(passwordField1.getPassword())))
                {
                    //everytime you click the button enter after a login
                    /**
                     * the keypads won't work and the withdraw setting will not work
                     */
                    setLimitLabel.setText("");
                    setLimitTextField.setEnabled(false);
                    numButton0.setEnabled(false);
                    numButton1.setEnabled(false);
                    numButton2.setEnabled(false);
                    numButton3.setEnabled(false);
                    numButton4.setEnabled(false);
                    numButton5.setEnabled(false);            
                    numButton6.setEnabled(false);
                    numButton7.setEnabled(false);
                    numButton8.setEnabled(false);
                    numButton9.setEnabled(false);
                    numButtonDecimal.setEnabled(false);
                    numButtonClear.setEnabled(false);
                    inquiryBtn.setEnabled(false);
                    enterButton.setEnabled(false);
                
                    //making a cLayout which is a new cardlayout that reprsents the mainPanel
                    CardLayout cLayout= (CardLayout)mainPanel.getLayout(); 
                    /**
                     * //this is to show the cardlayout which is cLayout to either 
                     * anycards or panel you want. in this case it is showing
                     * inquiryCard when the enter button is clicked
                     */
                    cLayout.show(mainPanel,"inquiryCard"); 
                    //replacing the current card state to inquiryCard
                    currentCard="inquiryCard";
                    /**
                     * This is to set the buttons to reactivate after the the login is validated.
                     */
                    depositButton.setEnabled(true);
                    withdrawButton.setEnabled(true);
                    backButton.setEnabled(true);
                    //setting the account number and type to the labels
                    textLabel8.setText(acc.getAccNum());
                    textLabel9.setText(acc.getAccType());
                    //getting the value of interest rate which is a float converting it to string and display to user
                    textLabel10.setText(String.valueOf(acc.getInterestRate()));
                    textLabel11.setText(String.valueOf(acc.getNewBalance()));
                    //if the account type is savings, then set the labels to be true and active
                    //users can set their withdrawal limit.
                    if(acc.getAccType().equalsIgnoreCase("savings"))
                    {
                        setLimitLabel.setText("Set Limit");
                        setLimitTextField.setEnabled(true);
                    }
                    errorText.setText("");
                    break;
                }
                else
                {
                    errorText.setText("Error, please key in your valid account");
                    break;
                }
                
                
            }
            case "withdrawCard":
            {
                //if the account type is savings, do this
                if(acc.getAccType().equalsIgnoreCase("savings"))
                    {
                        //setting the withdrawal limit to what users desire.
                        acc.setWithdrawalLimit(Integer.parseInt(setLimitTextField.getText()));
                    }
                //If ther withdraw amount is not 10 or 30 dollars, then proceed
                    if(withdrawTextField.getText()!="10"||withdrawTextField.getText()!="30")
                    {
                        /**
                         * if the amount requested to be withdrawn is more 
                         * than the balance, then display error message.
                         * Else proceed
                         */
                        if(Float.parseFloat(withdrawTextField.getText())>acc.getNewBalance()||Float.parseFloat(withdrawTextField.getText())>acc.getWithdrawalLimit())
                        {
                            errorText1.setText("Error, please key in less then your balance or withdraw limit");
                            break;
                        }
                        else
                        {  
                            /**
                             * getting the amount user wants to withdraw
                             * and setting it to the super class of "Account"
                             * */
                            acc.setCashOut(Float.parseFloat(withdrawTextField.getText()));
                            //Once the enter button is clicked and there is no 
                            //problem, then change the card to afterCashOut
                            //Where values will be displayed. Assuming we are
                            //currently at withdrawCard which is normally after inquiryCard
                            CardLayout cLayout= (CardLayout)mainPanel.getLayout(); 
                            cLayout.show(mainPanel,"afterCashOutCard"); 
                            currentCard="afterCashOutCard";
                            //call the function to execute some calculation
                            acc.getCashOutBalance();
                            //call the function to execute some calculation
                            cashOutAmountLabel.setText(String.valueOf(acc.getCashOut()));
                            /**
                            * Show the new balance after deposit and withdraw together
                            */
                            newBalanceLabel.setText(String.valueOf(acc.getNewBalance()));
                            withdrawButton.setEnabled(false);
                            inquiryBtn.setEnabled(true);
                            errorText1.setText("");
                            break;
                        }
                        
                    }
                    else
                    {
                        //display this when users what to cash out 10 or 30 dollars.
                        errorText1.setText("Error, You can only casah out 20, 50 and 100 dollars note");
                        break;
                    }
                    
                   
                
                 
            }
            case "depositCard":
            {
                    //set the deposit to what users desire.
                    acc.setDeposit(Float.parseFloat(depositTextField.getText()));
                    //Once the enter button is clicked and there is no 
                    //problem, then change the card to afterCashIn
                    //Where values will be displayed. Assuming we are now at 
                    //depositCard which is normally after inquiryCard
                    CardLayout cLayout= (CardLayout)mainPanel.getLayout(); 
                    cLayout.show(mainPanel,"afterCashInCard"); 
                    currentCard="afterCashInCard";
                    //call the function to execute some calculation
                    acc.getDepositBalance();
                    //show the deposit that you have previously made
                    cashInAmountLabel.setText(String.valueOf(acc.getDeposit()));
                    /**
                     * Show the new balance after deposit and withdraw together
                     */
                    newBalanceLabel1.setText(String.valueOf(acc.getNewBalance()));
                    withdrawButton.setEnabled(true);
                    inquiryBtn.setEnabled(true);
                   
                
                 break;
            }
            default:
                break;
            
        }
        
    }//GEN-LAST:event_enterButtonActionPerformed

