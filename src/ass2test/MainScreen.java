    private boolean isValidAccount(String accNum, String password)
    {
        //Where the validation is
        for (int i = 0; i < accounts.length; i++) {
             acc =accounts[i];
             //if the account number and password is equals to what has been passed, then return true
             if (acc.getAccNum().equals(accNum) && (acc.getPassword().equals(password)))
            {
                return true;
            }
           
        }
        return false;
    }
